#include <stdlib.h>
#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2);

static int	ft_len(char *str)
{
	int	len;

	len = 0;
	while (str[len] != '\0')
		len++;
	return (len);
}

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*start;
	char	*str;
	int		len;

	len = ft_len((char *)s1) + ft_len((char *)s2) + 1;
	str = (char *)malloc(len);
	if (str == NULL)
		return (NULL);
	start = str;
	while (*s1)
		*str++ = *s1++;
	while (*s2)
		*str++ = *s2++;
	*str = '\0';
	return (start);
}
